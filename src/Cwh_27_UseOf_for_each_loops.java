public class Cwh_27_UseOf_for_each_loops {
    
    /** 
     * @param args
     */
    public static void main(String[] args)
    {
        float[] marks = { 98.14f,45.2f,56.45f,85.36f,50.02f};

        for(float element: marks)
        {
            System.out.println(element);
        }
    }

}
